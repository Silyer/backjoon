#include <iostream>
#include <vector>
using namespace std;

int main() {
	int n;
	cin >> n;
	vector<long long> d(n + 1, 1);
	for(int i = 2; i < n; i++) d[i] = d[i - 2] + d[i - 1];
	cout << d[n - 1];
	
	return 0;
}
