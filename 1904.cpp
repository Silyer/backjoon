#include <iostream>
using namespace std;

int main() {
	int n;
	long long d[1000001] = { 0 };
	cin >> n;
	d[0] = 1;
	d[1] = 1;
	for (int i = 2; i <= 1000000; i++) {
		d[i] = d[i - 1] + d[i - 2];
		d[i] %= 15746;
	}
	cout << d[n] << '\n';
	return 0;
}
